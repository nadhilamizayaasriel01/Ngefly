    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
         String id = inputId.getText();
         String fullname = inputFullname.getText();
         String birthdate = inputBirthdate.getText();
         String age = inputAge.getText();
         String address = inputAddress.getText();
         String city = inputCity.getText();
         String contact = inputContact.getText();
         BiodataL bl = new BiodataL(id, fullname, birthdate, age, address, city, contact);

        if (fullname.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Add Fullname!");
        } else if (id.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Add id!");
        } else if(birthdate.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Add  Birthdate!");
        } else if (age.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Add Age!");
        } else if (address.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Add Address!");
        } else if (city.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Add City!");
        } else if (contact.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Add Contact!");
        } else if (btnSubmit.getText().equals("Submit")) {
            try {
                if (koneksiData.tambahDataBiodata(bl)) {
                    JOptionPane.showMessageDialog(this, "Add Data Success !");
                    populateTable();
                } else {
                    JOptionPane.showMessageDialog(this, "Add Data Failed !");
                }
            } catch (SQLException ex) {
                System.out.println("Eksepsi: " + ex.getMessage());
                JOptionPane.showMessageDialog(null, "Exception: " + ex.getMessage());
            }
    }//GEN-LAST:event_btnSubmitActionPerformed
    }
    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int selectedRow = tblCustomer.getSelectedRow();
        String id = model.getValueAt(selectedRow, 0).toString();
        int jawaban = JOptionPane.showConfirmDialog(this, "Data Delete " + id + "?", "Delete", JOptionPane.YES_NO_OPTION);
        if (jawaban == JOptionPane.YES_OPTION) {
            //hapus
            try {
                if (koneksiData.hapusDataBiodata(id)) {
                    JOptionPane.showMessageDialog(this, "Delete Success");
                    populateTable();
                } else {
                    JOptionPane.showMessageDialog(this, "Delete Failed");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Eksepsi: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

