       private void populateTable() {
        model.setRowCount(0);
        try {
            ArrayList<BiodataL> lihat = koneksiData.tampilDataBiodata();
            for (BiodataL bl : lihat) {
                Object[] isiData = {bl.getId(), bl.getFullname(), bl.getBirthdate(), bl.getAge(), bl.getAddress(), bl.getCity(), bl.getContact()};
                model.addRow(isiData);
            }
        } catch (SQLException ex) {
            System.out.println("Eksepsi: " + ex.getMessage());
            JOptionPane.showMessageDialog(null, "Exception: " + ex.getMessage());
        }

    }
    
